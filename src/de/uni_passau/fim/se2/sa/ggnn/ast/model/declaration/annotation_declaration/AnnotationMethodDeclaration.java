package de.uni_passau.fim.se2.sa.ggnn.ast.model.declaration.annotation_declaration;

import de.uni_passau.fim.se2.sa.ggnn.ast.model.AstNode;
import de.uni_passau.fim.se2.sa.ggnn.ast.model.annotation.ElementValue;
import de.uni_passau.fim.se2.sa.ggnn.ast.model.identifier.SimpleIdentifier;
import de.uni_passau.fim.se2.sa.ggnn.ast.model.type.Type;
import de.uni_passau.fim.se2.sa.ggnn.ast.visitor.AstVisitor;
import de.uni_passau.fim.se2.sa.ggnn.ast.visitor.Visitable;

import java.util.ArrayList;
import java.util.Collections;
import java.util.List;
import java.util.Optional;

public record AnnotationMethodDeclaration(Type type, SimpleIdentifier name, Optional<ElementValue> defaultValue)
    implements AnnotationMemberDeclaration, Visitable {

    @Override
    public <R, A> R accept(AstVisitor<R, A> visitor, A arg) {
        return visitor.visit(this, arg);
    }

    @Override
    public List<AstNode> children() {
        final List<AstNode> children = new ArrayList<>();
        children.add(type());
        children.add(name());
        defaultValue().ifPresent(children::add);
        return Collections.unmodifiableList(children);
    }
}
