package de.uni_passau.fim.se2.sa.ggnn.ast.model.expression.lambda;

import de.uni_passau.fim.se2.sa.ggnn.ast.model.AstNode;
import de.uni_passau.fim.se2.sa.ggnn.ast.model.expression.Expression;
import de.uni_passau.fim.se2.sa.ggnn.ast.model.statement.Block;
import de.uni_passau.fim.se2.sa.ggnn.ast.visitor.AstVisitor;
import de.uni_passau.fim.se2.sa.ggnn.ast.visitor.Visitable;

import java.util.List;

public interface LambdaExpr extends Expression {

    record BlockLambda(LambdaParameters parameters, Block body) implements LambdaExpr, Visitable {

        @Override
        public <R, A> R accept(AstVisitor<R, A> visitor, A arg) {
            return visitor.visit(this, arg);
        }

        @Override
        public List<AstNode> children() {
            return List.of(parameters(), body());
        }
    }

    record ExprLambda(LambdaParameters parameters, Expression body) implements LambdaExpr, Visitable {

        @Override
        public <R, A> R accept(AstVisitor<R, A> visitor, A arg) {
            return visitor.visit(this, arg);
        }

        @Override
        public List<AstNode> children() {
            return List.of(parameters(), body());
        }
    }
}
