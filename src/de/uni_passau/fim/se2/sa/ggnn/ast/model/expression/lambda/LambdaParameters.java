package de.uni_passau.fim.se2.sa.ggnn.ast.model.expression.lambda;

import de.uni_passau.fim.se2.sa.ggnn.ast.model.AstNode;
import de.uni_passau.fim.se2.sa.ggnn.ast.model.AstNodeList;
import de.uni_passau.fim.se2.sa.ggnn.ast.model.identifier.SimpleIdentifier;
import de.uni_passau.fim.se2.sa.ggnn.ast.visitor.AstVisitor;
import de.uni_passau.fim.se2.sa.ggnn.ast.visitor.Visitable;

import java.util.List;

public interface LambdaParameters extends AstNode {

    record IdentifierParameters(List<SimpleIdentifier> parameters)
            implements LambdaParameters, AstNodeList<SimpleIdentifier>, Visitable {

        @Override
        public <R, A> R accept(AstVisitor<R, A> visitor, A arg) {
            return visitor.visit(this, arg);
        }

        @Override
        public List<SimpleIdentifier> nodes() {
            return List.copyOf(parameters());
        }
    }
}
