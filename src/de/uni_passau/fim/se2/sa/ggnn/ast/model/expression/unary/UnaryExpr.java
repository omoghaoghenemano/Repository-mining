package de.uni_passau.fim.se2.sa.ggnn.ast.model.expression.unary;

import de.uni_passau.fim.se2.sa.ggnn.ast.model.AstNode;
import de.uni_passau.fim.se2.sa.ggnn.ast.model.expression.Expression;
import de.uni_passau.fim.se2.sa.ggnn.ast.visitor.AstVisitor;
import de.uni_passau.fim.se2.sa.ggnn.ast.visitor.Visitable;

import java.util.Collections;
import java.util.List;

public record UnaryExpr(UnaryExprType type, UnaryOperator op, Expression expression) implements Expression, Visitable {

    @Override
    public <R, A> R accept(AstVisitor<R, A> visitor, A arg) {
        return visitor.visit(this, arg);
    }

    @Override
    public List<AstNode> children() {
        return List.of(type(), op(), expression());
    }

    public enum UnaryExprType implements AstNode, Visitable {

        PREFIX,
        POSTFIX,
        ;

        @Override
        public <R, A> R accept(AstVisitor<R, A> visitor, A arg) {
            return visitor.visit(this, arg);
        }

        @Override
        public List<AstNode> children() {
            return Collections.emptyList();
        }
    }
}
